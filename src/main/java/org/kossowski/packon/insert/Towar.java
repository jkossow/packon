package org.kossowski.packon.insert;

public class Towar {

    private Long tw_Id;
    private String tw_Symbol;
    private String tw_Nazwa;


    public Long getTw_Id() {
        return tw_Id;
    }

    public void setTw_Id(Long tw_Id) {
        this.tw_Id = tw_Id;
    }

    public String getTw_Symbol() {
        return tw_Symbol;
    }

    public void setTw_Symbol(String tw_Symbol) {
        this.tw_Symbol = tw_Symbol;
    }

    public String getTw_Nazwa() {
        return tw_Nazwa;
    }

    public void setTw_Nazwa(String tw_Nazwa) {
        this.tw_Nazwa = tw_Nazwa;
    }
}
